    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (checkEmpty()==true){
            JOptionPane.showMessageDialog(rootPane, "Please complete all fields", "Incomplete Fields", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus(true);
            return;
        }
        int dialogButton = JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING", JOptionPane.YES_NO_OPTION);
        if (dialogButton == JOptionPane.YES_OPTION) {
            String Username = txtID.getText();

            ArrayList<String> tempArray = new ArrayList<>();
            try {
                try ( FileReader fr = new FileReader("Appointment.txt")) {
                    Scanner sc = new Scanner(fr);
                    String line;
                    String[] lineArr;

                    while ((line = sc.nextLine()) != null) {
                        lineArr = line.split("/");
                        if (!lineArr[0].equals(Username)) {
                            tempArray.add(line);
                        }
                    }
                    fr.close();
                } catch (Exception e) {
                }
            } catch (Exception e) {
            }
            try {
                try ( PrintWriter pr = new PrintWriter("Appointment.txt")) {
                    for (String str : tempArray) {
                        pr.println(str);
                    }
                    pr.close();
                    JOptionPane.showMessageDialog(rootPane, "Cancel Succesfully");
                } catch (Exception e) {
                }
            } catch (Exception e) {

            }
        } else {
            remove(dialogButton);
        }

        //clear the input after register
        txtID.setText("");
        txtPhone.setText("");
        txtEmail.setText("");
        txtAddress.setText("");
        AppDate.setText("");
        txtVaccine.setText("");
        txtVaccen.setText("");

        txtID.requestFocus();
    }//GEN-LAST:event_btnDeleteActionPerformed

