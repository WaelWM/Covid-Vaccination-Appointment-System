    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        // TODO add your handling code here:
        txtID.setText(Login.txtUserID.getText());

        String id = txtID.getText();
        try {
            BufferedReader br = new BufferedReader(new FileReader("Appointment.txt"));
            String str = "";
            while ((str = br.readLine()) != null) {
                String data[] = new String[6];
                data = str.split("/");

                if (data[0].equals(id)) {
                    txtPhone.setText(data[1]);
                    txtEmail.setText(data[2]);
                    txtAddress.setText(data[3]);
                    AppDate.setText(data[4]);
                    txtVaccine.setText(data[5]);
                    txtVaccen.setText(data[6]);

                    txtID.setEditable(false);
                    txtEmail.setEditable(false);
                    txtPhone.setEditable(false);
                    txtAddress.setEditable(false);
                    AppDate.setEditable(false);
                    txtVaccine.setEditable(false);
                    txtVaccen.setEditable(false);
                }
            }
        } catch (Exception e) {

        }

        try {
            filename = new FileReader("Appointment.txt");
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(rootPane, "No file found", "Error Loading File", JOptionPane.ERROR);
        }
    }//GEN-LAST:event_formWindowActivated

